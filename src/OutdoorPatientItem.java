    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try{
        String info="Insert into OutdoorPatientInfo (op_id,op_name,op_age,op_sex,op_address,op_mobile,assigned_doc,medicine,add_date) values(?,?,?,?,?,?,?,?,?)" ;
             pst=conn.prepareStatement(info);
             
              if((idField.getText() != null && !idField.getText().isEmpty())&& (nameField.getText() != null && !nameField.getText().isEmpty())
                 && (ageField.getText() != null && !ageField.getText().isEmpty())
                 &&(addressField.getText() != null && !addressField.getText().isEmpty()) && (mobileField.getText() != null && !mobileField.getText().isEmpty())
                 && (medicineField.getText() != null && !medicineField.getText().isEmpty()))
             
              { 
            pst.setString(1,idField.getText());
            pst.setString(2,nameField.getText());
            pst.setString(3,ageField.getText());
            String gender=combo_sex.getSelectedItem().toString();
            pst.setString(4,gender);
            pst.setString(5,addressField.getText());
            pst.setString(6,mobileField.getText());
            String doctor=combo_doc.getSelectedItem().toString();
            pst.setString(7,doctor);
            pst.setString(8,medicineField.getText());
            pst.setString(9,dateField.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"Saved");
            dispose();
              
            }
              else
              {
              JOptionPane.showMessageDialog(null,"Field connot be Empty");
              }
            
        
        }
        
        catch(Exception e)
        {
        JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_saveActionPerformed

